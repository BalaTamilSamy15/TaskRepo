package com.onesoft.day1;

public class AssignmentOperator {
	public static void main(String[] args) {
		int a = 12;
		a += 24;
		System.out.println("add the given value " + a);
		a /= 2;
		System.out.println("divide the given value " + a);
		a -= 2;
		System.out.println("subract the given value " + a);
		a %= 3;
		System.out.println("reminder value of given value " + a);
	}
}
